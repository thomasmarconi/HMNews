DROP TABLE IF EXISTS likes;

CREATE TABLE likes(
	id TEXT PRIMARY KEY,
	url TINYTEXT,
	title TINYTEXT,
	keywords MEDIUMTEXT,
	email TINYTEXT

);

DROP TABLE IF EXISTS dislikes;

CREATE TABLE dislikes(
	id TEXT PRIMARY KEY,
	url TINYTEXT,
	title TINYTEXT,
	keywords MEDIUMTEXT,
	email TINYTEXT
);

DROP TABLE IF EXISTS articles;

CREATE TABLE articles(
	id TEXT PRIMARY KEY,
	url TEXT,
	title TEXT
);
